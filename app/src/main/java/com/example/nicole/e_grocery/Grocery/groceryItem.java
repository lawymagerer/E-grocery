public class groceryItem {
    String grocery_name;
    String grocery_id;
    String grocery_image;
    String grocery_dropdown;
    String grocery_price;


    public String getGrocery_name() {
        return grocery_name;
    }

    public void setGrocery_name(String grocery_name) {
        this.grocery_name = grocery_name;
    }

    public String getGrocery_id() {
        return grocery_id;
    }

    public void setGrocery_id(String grocery_id) {
        this.grocery_id = grocery_id;
    }

    public String getGrocery_image() {
        return grocery_image;
    }

    public void setGrocery_image(String grocery_image) {
        this.grocery_image = grocery_image;
    }

    public String getGrocery_dropdown() {
        return grocery_dropdown;
    }

    public void setGrocery_dropdown(String grocery_dropdown) {
        this.grocery_dropdown = grocery_dropdown;
    }

    public String getGrocery_price() {
        return grocery_price;
    }

    public void setGrocery_price(String grocery_price) {
        this.grocery_price = grocery_price;
    }
}
